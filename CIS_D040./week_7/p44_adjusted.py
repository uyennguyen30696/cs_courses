'''
Program 44:

You have 1000 lockers and 1000 students. All lockers are initially locked.
1st student opens all lockers
2nd student closes every other locker
3rd student opens(if closed) or closes (if open) every 3rd locker
4th student opens(if closed) or closes (if open) every 4th locker
.
.
.
1000th student opens(if closed) or closes (if open) every 1000th locker

Write a program to determine which exact locker numbers are open, and total number that are open.
'''

# List of 1000 closed lockers
# 1 = locked, 0 = open
lockers = [] 
for locker in range(0, 1000):
    lockers.append(1)

# 1st student opens all lockers
for i in range(0, len(lockers)):
    lockers[i] = 0

# 2nd student closes every other locker
for i in range(1, len(lockers), 2):
    lockers[i] = 1

# 3rd student to 1000th student opens(if closed) or closes (if open) every 3rd (to 1000th) locker
for j in range(3, len(lockers)):
    for i in range(j - 1, 999, j):
        if lockers[i] == 1:
            lockers[i] = 0
        elif lockers[i] == 0:
            lockers[i] = 1

print (lockers)
print ('Number of locker:', len(lockers))

# # Count the number of open lockers
count = 0
for i in range(0, len(lockers)):
    if lockers[i] == 0:
        count = count + 1
        print('Locker number', i + 1,'is open')
print("There are", count, "lockers open")

